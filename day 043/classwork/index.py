# 1. for loop – 5-იდან 15-მდე ციფრები
print(". 5-იდან 15-მდე:")
for i in range(5, 16):  
    print(i)

# 2. for loop – 5-იდან 15-მდე, 2-ის გამოტოვებით
print(". 5-იდან 15-მდე, 2-ის გამოტოვებით:")
for i in range(5, 16):
    if i != 2:  
        print(i)

# 3. for loop – თითოეული რიცხვი 1-ით გაზრდილი
print(". 5-იდან 15-მდე, თითოეული +1:")
for i in range(5, 16):
    print(i + 1)  

# 4. კომენტარი: როგორ მუშაობს for loop
print(". ახსნა:")
# for loop გამოიყენება განმეორებითი მოქმედებების შესასრულებლად.
# მაგალითად, თუ გვინდა რიცხვების ბეჭდვა 5-დან 15-მდე:
for i in range(5, 16):  
    print(f"რიცხვი არის: {i}")  
# ციკლი იწყება 5-ით, შემდეგ გადადის 6-ზე, 7-ზე... და ასე 15-მდე.
# ყოველი ნაბიჯზე შესრულდება ციკლის შიგნით არსებული კოდი.