# 1. სიის ჯამი
numbers1 = [10, 20, 30, 40, 50]
print("1. ჯამი:", sum(numbers1))

# 2. უდიდესი რიცხვი
numbers2 = [5, 12, 3, 99, 45]
print("2. უდიდესი რიცხვი:", max(numbers2))

# 3. უმცირესი რიცხვი
numbers3 = [8, 2, 15, 6, 23]
print("3. უმცირესი რიცხვი:", min(numbers3))

# 4. სიის სიგრძე
numbers4 = [1, 2, 3, 4, 5]
print("4. სიის სიგრძე:", len(numbers4))

# 5. .append() – სიის ბოლოში დამატება
words1 = ["ვაშლი", "მსხალი", "ატამი", "ბანანი", "კივი"]
words1.append("ანანასი")
print("5. .append() შედეგი:", words1)

# 6. .insert() – კონკრეტულ ინდექსზე დამატება
words2 = ["ლურჯი", "წითელი", "ყვითელი", "მწვანე", "შავი"]
words2.insert(2, "თეთრი")
print("6. .insert() შედეგი:", words2)

# 7. .pop() – კონკრეტული ინდექსის ამოღება
words3 = ["კატა", "ძაღლი", "თაგვი", "ციყვი", "მელა"]
removed_word = words3.pop(3)
print("7. .pop() ამოღებული სიტყვა:", removed_word)
print("7. განახლებული სია:", words3)